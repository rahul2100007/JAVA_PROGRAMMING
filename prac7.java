class prac7
{
	public static void main(String[] args) 
	{
		System.out.println("EVEN NUMBERS");
		for (int i=1;i<=5;i++)
		{
			if (i%2==0)
			{
				System.out.println(i);
			}
		}
		System.out.println("ODD NUMBERS");
		for (int i=1;i<=5;i++)
		{
			if (i%2!=0)
			{
				System.out.println(i);
			}
		}
	}
}
